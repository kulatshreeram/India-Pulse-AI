export function useTranslation() {
  const language = useNewsStore((state) => state.language);
  
  const t = (key: string): string => {
    if (language === 'en') return key;
    const dict = DICTIONARY[language as 'hi' | 'mr'];
    if (dict && key in dict) {
      return dict[key as keyof typeof dict];
    }
    return key;
  };
  
  return { t, language };
}
